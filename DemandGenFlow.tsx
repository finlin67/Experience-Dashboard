'use client';

import React, { useState, useEffect, useMemo, useCallback } from 'react';
import { 
  BarChart2, 
  TrendingUp, 
  Activity,
  ArrowRight
} from 'lucide-react';
import { motion } from 'framer-motion';

/**
 * DemandGenFlow - A high-fidelity enterprise dashboard component.
 * Optimized for Framer Motion v12+ and strictly follows Animation Gallery constraints.
 */
export default function DemandGenFlow() {
  const [metrics, setMetrics] = useState({
    growthROI: 312,
    totalLeads: 52.4,
    conversionLift: 26.2,
    cplReduction: 44.5,
    iterationVelocity: 4.8,
    chartData: [0.3, 0.45, 0.35, 0.6, 0.85, 0.95, 0.7, 0.9],
    successRateHistory: Array.from({ length: 30 }, () => Math.floor(Math.random() * 15) + 70)
  });

  // Organic Jitter Update Logic via recursive setTimeout
  const updateMetrics = useCallback(() => {
    const drift = (val: number, range: number) => val + (Math.random() * range * 2 - range);
    
    setMetrics(prev => {
      const lastRate = prev.successRateHistory[prev.successRateHistory.length - 1];
      const newRate = Math.min(98, Math.max(65, drift(lastRate, 2.5)));

      return {
        growthROI: Math.floor(Math.max(280, Math.min(340, drift(prev.growthROI, 2)))),
        totalLeads: parseFloat(Math.max(48, Math.min(58, drift(prev.totalLeads, 0.04))).toFixed(1)),
        conversionLift: parseFloat(Math.max(22, Math.min(32, drift(prev.conversionLift, 0.15))).toFixed(1)),
        cplReduction: parseFloat(Math.max(38, Math.min(48, drift(prev.cplReduction, 0.2))).toFixed(1)),
        iterationVelocity: parseFloat(Math.max(4, Math.min(5.8, drift(prev.iterationVelocity, 0.04))).toFixed(1)),
        chartData: prev.chartData.map(h => Math.max(0.2, Math.min(1.0, drift(h, 0.06)))),
        successRateHistory: [...prev.successRateHistory.slice(1), newRate]
      };
    });

    const nextTick = 2000 + Math.random() * 1000;
    const timer = setTimeout(updateMetrics, nextTick);
    return timer;
  }, []);

  useEffect(() => {
    const timer = updateMetrics();
    return () => clearTimeout(timer);
  }, [updateMetrics]);

  // Performance-optimized SVG path calculation
  const chartConfig = useMemo(() => {
    const data = metrics.successRateHistory;
    const min = 50; 
    const max = 100;
    const range = max - min;
    const width = 100; 
    const height = 50; 

    if (data.length < 2) return { linePath: "", areaPath: "", lastPoint: { x: 0, y: 0 } };

    const points = data.map((val, i) => ({
      x: (i / (data.length - 1)) * width,
      y: height - ((val - min) / range * height)
    }));

    const linePath = `M ${points.map(p => `${p.x},${p.y}`).join(' L ')}`;
    const areaPath = `${linePath} L ${width},${height} L 0,${height} Z`;
    const lastPoint = points[points.length - 1];

    return { linePath, areaPath, lastPoint };
  }, [metrics.successRateHistory]);

  const cardHoverState = {
    y: -5,
    scale: 1.02,
    backgroundColor: 'rgba(30, 41, 59, 0.6)',
    boxShadow: '0 20px 40px -15px rgba(0, 0, 0, 0.5), 0 0 20px 0 rgba(59, 130, 246, 0.1)'
  };

  return (
    <div className="w-full h-full flex items-center justify-center bg-[#020617] p-2">
      <div className="w-full aspect-square max-w-[600px] bg-[#020617] text-slate-100 overflow-hidden relative font-sans flex flex-col shadow-2xl rounded-2xl border border-slate-800">
        
        {/* Ambient background glow */}
        <div className="absolute top-[-5%] right-[-5%] w-[300px] h-[300px] bg-blue-600/10 rounded-full blur-[80px] pointer-events-none" />
        <div className="absolute bottom-[-5%] left-[-5%] w-[250px] h-[250px] bg-indigo-600/5 rounded-full blur-[80px] pointer-events-none" />

        {/* Header */}
        <div className="px-5 py-4 border-b border-slate-800 flex items-center justify-between bg-slate-900/40 backdrop-blur-md z-30 shrink-0">
          <div>
            <div className="flex items-center gap-1.5 mb-0.5">
              <BarChart2 className="text-blue-500 w-3.5 h-3.5" />
              <span className="text-[9px] font-bold uppercase tracking-[0.2em] text-slate-500">Analytics Suite</span>
            </div>
            <h3 className="text-base font-bold tracking-tight text-white flex items-center gap-2">
              Demand Gen Flow <span className="px-1.5 py-0.5 rounded bg-blue-500/10 text-blue-500 text-[8px] font-black border border-blue-500/20">ENTERPRISE</span>
            </h3>
          </div>
          <div className="flex flex-col items-end">
            <span className="text-[9px] font-bold text-emerald-500 flex items-center gap-1.5">
              <span className="relative flex h-1.5 w-1.5">
                <motion.span 
                  animate={{ opacity: [0.3, 0.8, 0.3], scale: [1, 1.5, 1] }} 
                  transition={{ duration: 2, repeat: Infinity, repeatType: "mirror" as const }}
                  className="absolute inline-flex h-full w-full rounded-full bg-emerald-500" 
                />
                <span className="relative inline-flex rounded-full h-1.5 w-1.5 bg-emerald-500"></span>
              </span>
              LIVE SYNC
            </span>
          </div>
        </div>

        {/* Dashboard Content Grid */}
        <div className="flex-1 p-5 grid grid-cols-2 gap-3 overflow-y-auto scrollbar-hide z-10">
          
          {/* Card: Growth ROI */}
          <motion.div 
            whileHover={cardHoverState}
            className="col-span-1 bg-slate-900/30 border border-slate-800/60 rounded-xl p-4 flex flex-col justify-between group transition-colors duration-300"
          >
            <div>
              <span className="text-[9px] text-slate-500 font-bold uppercase tracking-widest block mb-1">Growth ROI</span>
              <div className="flex items-baseline gap-1">
                <span className="text-2xl font-bold text-white tracking-tight">{metrics.growthROI}</span>
                <span className="text-xs font-medium text-slate-500">%</span>
              </div>
            </div>
            <div className="h-8 w-full flex items-end gap-1 mt-3">
              {metrics.chartData.map((h, i) => (
                <motion.div 
                  key={i} 
                  animate={{ scaleY: [0, h] }} 
                  className="flex-1 bg-blue-500/30 rounded-t-[1px] origin-bottom group-hover:bg-blue-500/60 transition-colors" 
                />
              ))}
            </div>
          </motion.div>

          {/* Card: Lead Generation */}
          <motion.div 
            whileHover={cardHoverState}
            className="col-span-1 bg-slate-900/30 border border-slate-800/60 rounded-xl p-4 flex flex-col justify-between group transition-colors duration-300"
          >
            <div>
              <span className="text-[9px] text-slate-500 font-bold uppercase tracking-widest block mb-1">Lead Gen</span>
              <div className="flex items-baseline gap-1">
                <span className="text-2xl font-bold text-white tracking-tight">{metrics.totalLeads}</span>
                <span className="text-xs font-medium text-slate-500">k</span>
              </div>
            </div>
            <div className="mt-3">
              <div className="h-1.5 w-full bg-slate-800/50 rounded-full overflow-hidden">
                <motion.div 
                  animate={{ width: `${(metrics.totalLeads / 60) * 100}%` }} 
                  className="h-full bg-gradient-to-r from-indigo-500 to-blue-500" 
                />
              </div>
              <div className="flex justify-between mt-1.5 text-[8px] text-slate-500 font-bold uppercase tracking-tight">
                <span>Target: 60k</span>
                <span className="text-indigo-400">92% Met</span>
              </div>
            </div>
          </motion.div>

          {/* Card: Success Rate Trend Chart */}
          <motion.div 
            whileHover={cardHoverState}
            className="col-span-2 bg-slate-900/30 border border-slate-800/60 rounded-xl p-4 flex flex-col group min-h-[160px] transition-colors duration-300"
          >
            <div className="flex justify-between items-start mb-4">
              <div>
                <span className="text-[9px] text-slate-500 font-bold uppercase tracking-widest block mb-0.5">Success Rate Trend</span>
                <div className="flex items-baseline gap-2">
                  <span className="text-2xl font-bold text-white group-hover:text-blue-400 transition-colors">{metrics.successRateHistory[metrics.successRateHistory.length - 1].toFixed(1)}%</span>
                  <span className="text-[10px] font-bold text-emerald-500 flex items-center">
                    <TrendingUp className="w-2.5 h-2.5 mr-0.5" />
                    +4.2%
                  </span>
                </div>
              </div>
              <div className="p-1.5 rounded-lg bg-slate-800/50 text-slate-400">
                <Activity className="w-3.5 h-3.5" />
              </div>
            </div>
            <div className="flex-1 w-full relative">
              <svg className="w-full h-full overflow-visible" viewBox="0 0 100 50" preserveAspectRatio="none">
                <defs>
                  <linearGradient id="chartFillGradient" x1="0" y1="0" x2="0" y2="1">
                    <stop offset="0%" stopColor="#3b82f6" stopOpacity="0.2" />
                    <stop offset="100%" stopColor="#3b82f6" stopOpacity="0" />
                  </linearGradient>
                </defs>
                <motion.path 
                  d={chartConfig.areaPath} 
                  fill="url(#chartFillGradient)" 
                  animate={{ d: chartConfig.areaPath }} 
                />
                <motion.path 
                  d={chartConfig.linePath} 
                  fill="none" 
                  stroke="#3b82f6" 
                  strokeWidth="1.2" 
                  animate={{ d: chartConfig.linePath }} 
                />
                <motion.circle 
                  cx={chartConfig.lastPoint.x} 
                  cy={chartConfig.lastPoint.y} 
                  r="1.8" 
                  fill="#020617" 
                  stroke="#3b82f6" 
                  strokeWidth="1" 
                  animate={{ cx: chartConfig.lastPoint.x, cy: chartConfig.lastPoint.y }} 
                />
              </svg>
            </div>
          </motion.div>

          {/* Mini Performance Badges */}
          <motion.div 
            whileHover={{ ...cardHoverState, scale: 1.05 }}
            className="col-span-1 bg-slate-900/30 border-l-2 border-l-emerald-500 border-slate-800/60 rounded-r-xl p-4 flex flex-col justify-center transition-colors duration-300"
          >
            <span className="text-[9px] text-slate-500 font-bold uppercase tracking-widest mb-1">Conv. Lift</span>
            <span className="text-xl font-bold text-white">+{metrics.conversionLift}%</span>
          </motion.div>

          <motion.div 
            whileHover={{ ...cardHoverState, scale: 1.05 }}
            className="col-span-1 bg-slate-900/30 border-l-2 border-l-indigo-500 border-slate-800/60 rounded-r-xl p-4 flex flex-col justify-center transition-colors duration-300"
          >
            <span className="text-[9px] text-slate-500 font-bold uppercase tracking-widest mb-1">CPL Reduction</span>
            <span className="text-xl font-bold text-white">-{metrics.cplReduction}%</span>
          </motion.div>

        </div>

        {/* Footer Action Bar */}
        <div className="px-5 py-4 bg-slate-900/60 border-t border-slate-800 flex items-center justify-between z-20 shrink-0">
          <div className="flex items-center gap-3">
            <div className="flex -space-x-1.5">
              {[1, 2, 3].map(i => (
                <div key={i} className="w-6 h-6 rounded-full border-2 border-slate-900 bg-slate-800 flex items-center justify-center text-[7px] font-bold text-slate-400">
                  U{i}
                </div>
              ))}
            </div>
            <span className="text-[10px] text-slate-500 font-medium">Synced 2s ago</span>
          </div>
          <motion.button 
            whileHover={{ scale: 1.05 }}
            whileTap={{ scale: 0.95 }}
            className="px-4 h-8 rounded-lg bg-blue-600 hover:bg-blue-500 text-white font-bold text-[10px] transition-all flex items-center gap-1.5 shadow-lg shadow-blue-900/20 active:scale-95"
          >
            Full Report
            <ArrowRight className="w-3 h-3" />
          </motion.button>
        </div>
      </div>
    </div>
  );
}
